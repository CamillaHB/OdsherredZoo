<h1>Parken</h1>

<p>Odsherred Zoo ligger midt i det smukke Odsherred. Parken er en oase på 14 tønder land og indbyder til mange dejlige timer i dyrenes verden. Odsherred Zoo har over 600 dyr fordelt på 100 arter.</p>
<p>I Odsherred Zoo kommer du tæt på dyrene, så tæt, at du både kan se, høre og lugte dyrene. Der er ingen tykke glas og meter høje hegn mellem dyr og publikum. I parken går påfugle, kaniner, høns og børn frit. Du oplever vores dejlige aber på tæt hold. Kun hvor det er absolut nødvendigt har vi sikkerhedshegn, så ingen får fingrene ind de forkerte steder.</p>
<p>At præsenterer dyrene på denne måde kræver tillid til vores gæster og respekt fra vores gæster. Det vil sige at man respekterer at der ikke på fodres af medbragt mad og ingen genstande stikkes ind igennem hegn til dyrene så de kommer til skade. Hos dådyrene er det dog et stort his hos både børn og dådyr at fodre med det omkring voksende græs.</p>

