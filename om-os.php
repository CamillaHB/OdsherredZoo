<h1>Om os</h1>



<h2>Historien</h2>

<p>For 20 år siden, i midten af 1990’erne, solgte ægteparret Jørgen og Joan Ankergren deres reklamebureau i Valby og investerede deres penge og energi i Odsherred Zoo. Udgangspunktet var, at borgmester Finn Madsen i den daværende Dragsholm Kommune havde fået den ide, at man havde behov for en turistattraktion. Han åbnede et menageri, hvor dyrene blev passet af unge i et beskæftigelsesprojekt. Men en dag stod man altså og manglede en driftig direktør, 'og da min søn på 8 år plagede om at få en hamster, sagde jeg til ham: du kan få en hel dyrepark', fortæller Jørgen Ankergren.</p>
<p>Siden 2008 har parken også fungeret som rescuecenter, hvor eksotiske dyr, der som følge af enten dyreforsøg eller vanrøgt, er blevet bragt på fode og har fået et nyt liv. Her kan traumatiserede og skadede dyr komme på fode igen. Nogle dyr lever livet »bag kulisserne« skærmet for publikums nysgerrige blikke, mens andre, deriblandt en stor flok java kakakker fra et belgisk laboratorium, boltrer sig i eget anlæg.</p>
    
<h2>Rescue Zoo</h2>

<h3>“Danmarks første zoo og rescuecenter for eksotiske dyr.”</h3>

<p>Der sidder dyr overalt i verden, der er i nød, og i Europa hvor velfærden er størst er der rigtig mange. Vores trang til kæledyr, konstante avl af dyrebørn i zoologiske haver, laboratorie- og forsøgsdyr har bidraget væsentlig til de mange dyr i nød. Vi må alle påtage os ansvar og handle på det.</p>
<p>I Odsherred Zoo har vi påtaget os et ansvar ”bag kulissen”. Vi har gennem de sidste 10 år modtaget dyr fra forskellige samrarbejdspartnere. Rescuecentre og forsøgscentre i Europa, Falck, Dyrenes beskyttelse i Danmark og private der ikke længere kan passe deres eksotiske kæledyr uden at modtage penge for det, blot gjort det når der var plads og midler. Ofte er det sådan at rescuecentrene er meget fattige og hvis du siger ja til at modtage dyrene så siger du ja til at betale transporten, nye anlæg, pasning af dyrene og evt. medicinering uden beregning.</p>
<p>Dette samarbejde har taget om sig og vi bliver nødt til at gå ud med det offentligt og søge om hjælp og støtte til projektet fra fonde, firmasponsorer og privatsponsorer og her hjælper alle beløb fra 50-50000kr. Odsherred Zoo tager skridtet fuldt ud og kalder os fra i dag Odsherred Zoo rescue og bliver dermed også Danmarks første rescuecenter for eksotiske dyr, samtidig gør vi op med, at publikum kun må se perfekte dyr i samlingen af zoodyr. For at modtage dyrene sætter vi som krav, at dyrene skal have en god livskvalitet, det vil sige ingen smerter, samkvem med andre af sin egen art eller andet selskab. Evt. sygdom skal kunne behandles og der skal være livsglæde/livskvalitet eller mulighed for at få det. En anden vigtig forebyggende ting er, at vi som zoologisk have skal gøre op med os selv, at vi ikke skal have alle arter, men specialiserer os og vi kan/skal ikke avle på alle dyre grupper for enhver pris, men oprette hangrupper eller hungrupper, så vi også får stoppet den uendelige aflivning af dyr, der konstant foregår. Kastrering, sterilisation eller fødselskontrol bør bruges med omtanke, men er absolut en gangbar løsning ved dyr, der i forvejen er for mange af i zoologiske haver.</p>
<p>Hjælp os med at hjælpe.</p>    

